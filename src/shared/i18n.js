function setText(selector, value) {
    document.querySelectorAll(selector).forEach((element) => {
        element.textContent = value;
    });
}

export function initLanguageSystem() {
    const savedLang = localStorage.getItem("forest-site-lang");
    const lang = savedLang === "en" ? "en" : "zh";
    const config = translations[lang];

    document.documentElement.lang = config.htmlLang;
    document.title = config.title;
    document.body.classList.toggle("lang-en", lang === "en");
    document.body.classList.toggle("lang-zh", lang === "zh");

    Object.entries(config.text).forEach(([selector, value]) => {
        setText(selector, value);
    });

    const toggle = document.querySelector(".lang-toggle");
    toggle.textContent = config.toggle;
    toggle.setAttribute("aria-label", config.toggleLabel);
    toggle.addEventListener("click", () => {
        localStorage.setItem("forest-site-lang", lang === "en" ? "zh" : "en");
        window.location.reload();
    });
}
